public class Player {

  private String name;
  public int lives;
  public String guess;

  public Player(String name) {
    this.name = name;
    this.lives = lives;
    this.guess = guess;
  }

  public String getName() {
    return name;
  }

  public int getLives() {
    return lives;
  }

  public String guess() {
    return guess;
  }
}
